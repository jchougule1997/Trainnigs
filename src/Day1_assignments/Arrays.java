package Day1_assignments;

public class Arrays {

	public static void main(String[] args) {

		int a[] = { 10, 20, 30, 40 };

		System.out.println(a[0]); // 10

		System.out.println(a[1]); // 20

		System.out.println(a[2]); // 30

		System.out.println(a[3]); // 40

		// Additions of Arrays

		System.out.println(a[0] + a[2]); // 40

	}

}
